create table if not exists website_monitors (
  id bigint generated by default as identity primary key,
  name text not null,
  url text not null,
  method text not null default 'GET',
  expected_status_min integer not null default 200,
  expected_status_max integer not null default 399,
  interval_sec integer not null default 120,
  timeout_sec integer not null default 10,
  grace_period_sec integer not null default 180,
  enabled boolean not null default true,
  hidden boolean not null default false,
  sort_order integer not null default 0,
  status text not null default 'pending',
  last_checked_at timestamptz,
  last_success_at timestamptz,
  last_failure_at timestamptz,
  last_status_code integer,
  last_raw_status_code integer,
  last_latency_ms integer,
  last_effective_reason text,
  last_error text,
  down_since timestamptz,
  last_notified_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint website_monitors_method_check check (method in ('GET', 'HEAD', 'TCP')),
  constraint website_monitors_status_check check (status in ('pending', 'up', 'down', 'paused')),
  constraint website_monitors_expected_status_check check (
    expected_status_min between 100 and 599
    and expected_status_max between 100 and 599
    and expected_status_min <= expected_status_max
  ),
  constraint website_monitors_interval_check check (interval_sec between 60 and 86400),
  constraint website_monitors_timeout_check check (timeout_sec between 1 and 30 and timeout_sec <= interval_sec),
  constraint website_monitors_grace_check check (grace_period_sec between 30 and 86400)
);

